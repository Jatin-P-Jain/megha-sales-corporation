// (Run this before calling changePricing, or build the map already-normalized)
function buildNormalizedPriceMap(
  raw: Record<string, number>,
): Record<string, number> {
  const out: Record<string, number> = {};
  for (const [k, v] of Object.entries(raw)) {
    const nk = normalizePartNumber(k);
    if (!nk) continue;
    out[nk] = v;
  }
  return out;
}

