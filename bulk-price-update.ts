async function changePricing(
  brandId: string,
  priceList: Record<string, number>,
) {
  const notMatched = [];
  const priceByPartNumber: Record<string, number> = priceList;
  const productsCol = sourceDb.collection("products");
  const snapshot = await productsCol.get();

  let batch = sourceDb.batch();
  let ops = 0;
  let updatedCount = 0;

  // Optional: useful debug
  let scanned = 0;
  let brandMatched = 0;
  let priceMatched = 0;

  for (const doc of snapshot.docs) {
    scanned++;

    const data = doc.data() as {
      brandId?: string;
      partNumber?: string;
      price?: unknown;
    };

    if (data?.brandId !== brandId) continue;
    brandMatched++;

    const normalizedDbPn = normalizePartNumber(data.partNumber);
    if (!normalizedDbPn) continue;

    // Map keys should be normalized using the same rule:
    const newPrice = priceByPartNumber[normalizedDbPn];
    if (newPrice === undefined) {
      notMatched.push(normalizedDbPn);
      continue;
    }
    priceMatched++;

    // If you must store price as string, use: `${newPrice}`
    const updates: FirebaseFirestore.UpdateData<{ [field: string]: any }> = {
      price: newPrice,
    };

    console.log(
      `${DRY_RUN ? "DRY RUN: " : ""}${doc.id} partNumber=${data.partNumber} -> ${normalizedDbPn} price=${newPrice}`,
    );

    if (!DRY_RUN) {
      batch.update(doc.ref, updates);
      ops++;

      if (ops >= 450) {
        await batch.commit();
        batch = sourceDb.batch();
        ops = 0;
      }
    }

    updatedCount++;
  }

  if (!DRY_RUN && ops > 0) {
    await batch.commit();
  }

  console.log(JSON.stringify(notMatched), notMatched.length);

  console.log(
    `✅ Done. scanned=${scanned}, brandMatched=${brandMatched}, priceMatched=${priceMatched}, updated=${updatedCount} ${DRY_RUN ? "(DRY RUN)" : ""}`,
  );
}

